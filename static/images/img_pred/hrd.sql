    DECLARE startDate DATE DEFAULT '2023-12-02';
    DECLARE endDate DATE DEFAULT '2023-12-31';
    DECLARE currentDate DATE;
    DECLARE i INT DEFAULT 0;
    DECLARE status VARCHAR(50);

