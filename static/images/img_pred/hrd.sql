            SET status = CASE FLOOR(RAND() * 4)
                            WHEN 0 THEN 'Hadir'
                            WHEN 1 THEN 'Sakit'
                            WHEN 2 THEN 'Alpa'
                            WHEN 3 THEN 'Izin'
                        END;

            -- Masukkan data jam masuk dan keluar serta catatan absensi
            INSERT INTO Kehadiran (KaryawanID, Tanggal, StatusKehadiran, JamMasuk, JamKeluar, Catatan)
            VALUES (
                @KaryawanID, 
                currentDate, 
                status,
                CASE 
                    WHEN status = 'Hadir' THEN TIME('08:00:00' + INTERVAL FLOOR(RAND() * 61) MINUTE)
                    ELSE NULL 
                END,
                CASE 
                    WHEN status = 'Hadir' THEN TIME('17:00:00' + INTERVAL FLOOR(RAND() * 61) MINUTE) 
                    ELSE NULL 
                END,
                CASE 
                    WHEN status = 'Hadir' AND TIME('08:00:00' + INTERVAL FLOOR(RAND() * 61) MINUTE) > '08:00:00' 
