                    THEN 'Karyawan Sakit'
                    WHEN status = 'Izin' 
                    THEN 'Izin untuk urusan pribadi'
                    WHEN status = 'Alpa' 
                    THEN 'Tidak hadir tanpa keterangan'
                    ELSE NULL 
                END
            );
            SET i = i + 1;
        END WHILE;

        SET currentDate = DATE_ADD(currentDate, INTERVAL 1 DAY);
    END WHILE;

    -- Drop temporary table
    DROP TEMPORARY TABLE TempKaryawanList;
